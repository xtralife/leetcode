package com.github.xtralife.leetcode.problems.p708.insert_into_a_sorted_circular_linked_list;

public class Node {
    public int val;
    public Node next;

    public Node() {
    }

    public Node(int val) {
        this.val = val;
    }

    public Node(int val, Node next) {
        this.val = val;
        this.next = next;
    }
}
